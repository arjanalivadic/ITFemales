                razgovarale [na sastancima] ... i uvijek je bilo teško natjerati muškarce da se usredotoče na ono što govorimo, a ne naše noge i dojke. " Dala je otkaz,
                doktorirala iz države San Francisco i 1995. godine osnovala vlastitu kompaniju za igračke Girl Tech. Swanson se u početku borila da pronađe investitore i dobavljače. „Dvije godine nakon što sam osnovala kompaniju, kupci trgovina igračkama rekli bi:„ Možete li to učiniti ružičastim “[ili]„ Možete li to napraviti za dječake? “A ja bih rekla: „Ne, to je ono sa čim se djevojke vole igrati”. Nije bilo lako, ali Swanson je 1998. godine prodala Girl Tech kompaniji Radica Games Limited za 6 miliona dolara.
                U kompaniji je ostala dvije godine kao potpredsjednica.</p>
