                <h3>Radia Perlman</h3>
                <span class="mb-4 d-block"></span>
                <p> Ko će javiti vijest Al Goreu? Radia Perlman, često opisana kao "majka Interneta", insistira na tome da "Internet nije izmislio nijedan pojedinac." Perlman je, međutim, stvorila algoritam iza protokola Spanning Tree Protocol (STP), koji je bitan dio temelja Interneta. Kao dijete Perlman je vršila veliki pritisak na sebe da dobije sve 5-ice. Iako se istakla u svim predmetima,
                prirodno je naginjala matematici i nauci jer je mogla kontrolirati ocjene samo znajući pravi odgovor.  </p>
                <p>Iako je njezina majka bila matematičarka / programerka, slično ženama iz ENIAC-a, njih dvije rijetko su govorile o programiranju.
                Perlman je otišla na MIT, samo jedna od šačice žena u njenom razredu (~ 50 od 1.000 učenika). 2000. Perlman je objavila svoj udžbenik „Interkonekcije“,
                koji je znatno pojednostavio mrežno usmjeravanje i premošćivanje. "Moja knjiga je stvorila red", rekla je kasnije. "Bilo je lako razumjeti dok sam konceptualno
                poticala na razmišljanje, a velik dio opisane tehnologije bila je stvar koju sam izmislila." Uprkos svom uspjehu,
                Perlman je realistična i jednako smatra ostale odgovornim za njezin uspjeh: „U inženjerstvu je poanta u tome da se posao završi i ljudi rado pomažu.
                Trebali biste biti velikodušni s kreditima i trebali biste rado pomagati drugima”.</p>
