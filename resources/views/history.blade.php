                <h3>Susan Kare</h3>
                <span class="mb-4 d-block"></span>
                <p>Kratko je radila za Microsoft, Susan Kare je najpoznatija po svom radu sa neprijateljem Billa Gatea, Steveom Jobsom.
                Kare je slijedila snove o karijeri u likovnoj umjetnosti do San Francisca. Slučajan susret sa starom srednjoškolskom prijateljicom doveo je do intervjua s Appleom.
                Steve Jobs, nadahnut Xeroxovim grafičkim korisničkim sučeljem (GUI), bio je u potrazi za umjetnikom koji bi mogao dizajnirati Macintoshove ikone.
                Dobila je posao. "Moral je imati povjerenje u vašu kombinaciju vještina", rekla je kasnije. "Zato što sigurno nisam imala računarsku pozadinu." </p>
                <p>Koristeći blok milimetrskog papira, Kare je dizajnirala ikone koje su bile jednostavne, elegantne i razigrane. Originalni dizajn bio je samo 32 x 32 piksela.
                Kare je također odgovorna za razvoj naredbenog ("Apple") ključa kao stiliziranog zamka gledanog odozgo. Kao dio svog Appleovog rada, Kare je stvorila tipografski
                slog Chicago, korišten u prve četiri generacije iPoda. Da bi natpisi ostali glatki i neprimjetni, sve linije su namjerno napravljene vodoravno, okomito ili
                pod uglom od 45 stepeni. Takva pažnja prema detaljima nastavila je definirati Apple do danas.</p>
