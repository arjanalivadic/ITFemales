                <span class="mb-4 d-block"></span>
                <p>U dobi od 15 godina promijenila je datum rođenja i započela slaganje televizora u Sears-u. Mnogo godina kasnije, Swanson će iskoristiti svoje iskustvo elektroničke prodaje kao voditeljica odjela za računare i tehnologiju u maloj trgovini zvanoj “Sudbina mog djeteta”. Zainteresovana za vezu tehnologije i obrazovanja djece, od Berkeley-a je dobila potvrdu o računaru u obrazovanju, sve dok je odgajala kćer. Swanson je 1988. godine počela raditi za softversku kompaniju Broderbund,
